/**
 * An abstract class that is to be used as the template for the Pokemon class.
 * 
 * @author Nikolas Beltran
 * @version 2015.05.30
 */
public abstract class PokemonTemplate
{
    /**
     * Returns the Pokedex entry.
     * 
     * @return  The Pokedex entry associated with the Pokemon.
     */
    abstract public String returnPokedexEntry();
}
